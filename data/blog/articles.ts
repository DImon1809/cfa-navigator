export function getArticlesByCategory(category: Category): Article[] {
  return articles.filter((a) => a.category === category && !a.draft);
}

export function getAllPublished(): Article[] {
  return articles.filter((a) => !a.draft);
}

export function getArticle(category: Category, slug: string): Article | undefined {
  return articles.find((a) => a.category === category && a.slug === slug);
}
